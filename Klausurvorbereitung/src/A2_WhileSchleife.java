public class A2_WhileSchleife {
    public static void main(String[] args) {

        int i = 0;

        while (i < 20) {
            System.out.print("#");
            i++;
        }
        System.out.println();

        i = 0;
        while (i < 20) {
            System.out.println("#");
            i++;
        }

        i = 0;
        while (i < 20) {
            System.out.print("#  ");
            i++;
        }
    }
}
